def default_extract_helper(completion: str, language: NullableLang = '', custom_extract_logic: Optional[str] = None):
    """Main extraction entry point: extract the best code block from a completion.

    Applies multiple extraction strategies in order of reliability: fenced code
    blocks (priority 30), heuristic extraction (priority 10), incomplete fenced
    blocks (priority 20), and optionally custom extraction logic. From the
    collected blocks, selects the highest-priority group, then returns the first
    block matching the target language; falls back to the first block of any
    language if no language match is found.

    Args:
        completion: The raw LLM completion text to extract code from.
        language: The target canonical language name to prefer when selecting
            among extracted blocks. Empty string means no language preference.
        custom_extract_logic: Optional Python code string for custom extraction
            (passed to :func:`extract_custom_code`).

    Returns:
        The extracted code string, or empty string if no code was found.
    """
    code_blocks = extract_fenced_code(completion)
    code_blocks += extract_heuristic_code(completion, language)
    code_blocks += extract_incomplete_fenced_code(completion)
    if custom_extract_logic is not None:
        code_blocks += extract_custom_code(completion, custom_extract_logic)
    if len(code_blocks) == 0:
        return ''

    max_priority = max([cb.priority for cb in code_blocks])
    code_blocks = [cb for cb in code_blocks if cb.priority == max_priority]

    target_blocks = filter_language(code_blocks, language)
    if len(target_blocks) > 0:
        return target_blocks[0].code
    return code_blocks[0].code


def remove_entripoints(code, language: NullableLang = ''):
    """Strip common entry point patterns from extracted code.

    Removes language-specific main entry points:

    - **Python**: Removes ``if __name__ == "__main__":`` block and
      ``# Example usage`` sections.
    - **C++**: Removes ``int main()`` and everything after.
    - **Go**: Removes ``package main`` declaration.

    Args:
        code: The source code string to clean.
        language: The canonical language name to determine which patterns
            to remove.

    Returns:
        The code with entry point patterns removed.
    """
    if language == 'python':
        if 'if __name__ == \"__main__\":' in code:
            next_line = code.index('if __name__ == \"__main__\":')
            code = code[:next_line].strip()
    elif language == 'cpp':
        if 'int main()' in code:
            next_line = code.index('int main()')
            code = code[:next_line].strip()
    elif language == 'go':
        # Remove package main
        code = code.replace('package main', '')
    if '# Example usage' in code:
        next_line = code.index('# Example usage')
        code = code[:next_line].strip()
    return code


# compatible function for evals/evals/elsuite/utils/coding_evaluation/utils_coding/extract_code_from_freeform_completion
def extract_code_from_freeform_completion(completion: str,
                                          language: NullableLang = '',
                                          first_block_only=False,
                                          **kwargs) -> Tuple[str, str]:
    """Legacy v1 extraction: extract code from freeform LLM completion with post-processing.

    Tries fenced blocks first, then incomplete fenced, then heuristic extraction.
    Applies language-specific post-processing including:

    - **Python**: Optionally removes assert statements; strips ``__main__`` blocks.
    - **C++**: Strips ``int main()`` and everything after.
    - **Java**: Wraps code in ``class Solution``, handles imports from declaration.
    - **Go**: Removes ``package main``.

    Args:
        completion: The raw LLM completion text.
        language: Target canonical language name.
        first_block_only: If True, use only the first extracted code block
            instead of concatenating all blocks.
        **kwargs: Additional options including:
            - ``is_fewshot_task`` (bool): Select first matching block for few-shot tasks.
            - ``exactly_match`` (bool): Drop blocks with non-matching language tags.
            - ``code_block_idx`` (int): Select a specific block by index.
            - ``remove_asserts`` (bool): Remove top-level assert statements (Python).
            - ``entry_point`` (str): Function name for Java class wrapping.
            - ``declaration`` (str): Import declarations to prepend (Java).

    Returns:
        A tuple of ``(code, extracted_type)`` where ``extracted_type`` is
        one of the ``ExtractedType`` enum values as a string.
    """
    extracted_type = ExtractedType.Empty  # initialize to empty case

    # step1. match the complete fenced block
    code_blocks = extract_fenced_code(completion)

    if kwargs.get('is_fewshot_task') is True:
        first_sp_block_idx = next((i for i, block in enumerate(code_blocks) if block.language == language), -1)
        first_un_block_idx = next((i for i, block in enumerate(code_blocks) if block.language == ''), -1)
        first_block_idx = first_un_block_idx if first_sp_block_idx == -1 else first_sp_block_idx
        if first_block_idx != -1:
            code_blocks = code_blocks[:first_block_idx + 1]
            code_blocks = code_blocks[:first_block_idx + 1]
        logger.debug(f'select first code block for fewshot task: {code_blocks}')

    # drop the blocks which the language tag different with target programming language
    if kwargs.get('exactly_match') == True and language:
        other_tag = set(sum([v for k, v in language_to_aliases.items() if k != language], []))
        code_blocks = [b for b in code_blocks if b.language not in other_tag]

    if code_blocks:
        extracted_type = ExtractedType.Fenced

    # step2. if no complete fenced block found, then match the incomplete fenced block
    if len(code_blocks) == 0:
        code_blocks = extract_incomplete_fenced_code(completion)
        if code_blocks:
            extracted_type = ExtractedType.IncompleteFenced

    # step3. if no incomplete fenced block found, try heuristic method to extract code
    if len(code_blocks) == 0:
        code_blocks = extract_heuristic_code(completion, language)
        if code_blocks:
            extracted_type = ExtractedType.Heuristic

    if kwargs.get('code_block_idx') is not None:
        try:
            completion = code_blocks[kwargs['code_block_idx']].code.replace('\r', '')
        except:
            completion = ''
    elif first_block_only:
        if code_blocks:
            completion = code_blocks[0].code.replace('\r', '')
        else:
            completion = ''
    else:
        completion = '\n\n'.join([b.code for b in code_blocks]).replace('\r', '')

    if language == 'python':

        if kwargs.get('remove_asserts') is True:
            #remove assert statements
            lines = []
            for line in completion.split('\n'):
                if line.startswith('assert '):
                    continue
                else:
                    lines.append(line)
            completion = '\n'.join(lines)

        if 'if __name__ == \"__main__\":' in completion:
            next_line = completion.index('if __name__ == \"__main__\":')
            completion = completion[:next_line].strip()
    elif language == 'cpp':
        if 'int main()' in completion:
            next_line = completion.index('int main()')
            completion = completion[:next_line].strip()
    elif language == 'java':
        # Add class Solution before signature
        if 'public class Main {\n' in completion:
            completion = completion.replace('public class Main {\n', 'class Solution {\n')
            completion = completion.replace('public static void main(String[] args)', '')
        if 'class Solution' not in completion:
            for line in completion.split('\n'):
                if kwargs.get('entry_point') and kwargs.get('entry_point') in line:
                    completion = completion.replace(line, 'class Solution {\n' + line)
                    completion += '\n}'
                    break
        # Add import statements
        for line in kwargs.get('declaration', '').split('\n'):
            if 'import' in line:
                completion = line + '\n' + completion
    elif language == 'go':
        # Remove package main
        completion = completion.replace('package main', '')
    if '# Example usage' in completion:
        next_line = completion.index('# Example usage')
        completion = completion[:next_line].strip()

    return (completion, extracted_type.value)


def extract_code_from_freeform_completion_v2(completion: str,
                                             language: NullableLang = '',
                                             first_block_only=False,
                                             no_removal=False,
                                             **kwargs) -> Tuple[str, str]:
    """Enhanced v2 extraction: extract code from freeform LLM completion.

    Improved version that handles llama3 formatting quirks (adjacent empty
    fenced blocks, language tag as first code line). Applies
    :func:`postprocess_completion_v2` for language-specific cleanup unless
    ``is_ut`` is True (unit test mode skips post-processing).

    Args:
        completion: The raw LLM completion text.
        language: Target canonical language name.
        first_block_only: If True, use only the first extracted code block.
        no_removal: If True, skip certain removal steps in post-processing
            (e.g., preserving ``package main`` in Go).
        **kwargs: Additional options including:
            - ``inner_function_only`` (bool): Extract only the inner function
              body (used for C#, Java).
            - ``is_ut`` (bool): If True, skip post-processing (unit test mode).
            - ``is_fewshot_task`` (bool): Select first matching block for
              few-shot tasks.
            - ``exactly_match`` (bool): Keep only blocks matching the target
              language tag.
            - ``code_block_idx`` (int): Select a specific block by index.
            - ``remove_asserts`` (bool): Remove assert statements (Python).
            - ``entry_point`` (str): Function name for Java class wrapping.
            - ``declaration`` (str): Import declarations to prepend (Java).

    Returns:
        A tuple of ``(code, extracted_type)`` where ``extracted_type`` is
        one of the ``ExtractedType`` enum values as a string.
    """
    completion_bk = completion  # backup the input
    extracted_type = ExtractedType.Empty  # initialize to empty case

    # step0. preprocess
    completion = completion.replace('```\n```', '```')  # solve llama3 error format

    # step1. match the complete fenced block
    code_blocks = extract_fenced_code(completion)
    code_blocks = adjust_code_block(code_blocks, language)  # solve llama3 error format

    if kwargs.get('is_fewshot_task') is True:
        first_sp_block_idx = next((i for i, block in enumerate(code_blocks) if block.language == language), -1)
        first_un_block_idx = next((i for i, block in enumerate(code_blocks) if block.language == ''), -1)
        first_block_idx = first_un_block_idx if first_sp_block_idx == -1 else first_sp_block_idx
        if first_block_idx != -1:
            code_blocks = code_blocks[:first_block_idx + 1]
            code_blocks = code_blocks[:first_block_idx + 1]
        logger.debug(f'select first code block for fewshot task: {code_blocks}')

    # drop the blocks which the language tag different with target programming language
    if kwargs.get('exactly_match') == True and language:
        target_tag = language_to_aliases.get(language, [])
        code_blocks = [b for b in code_blocks if b.language in target_tag]

    if code_blocks:
        extracted_type = ExtractedType.Fenced

    # step2. if no complete fenced block found, then match the incomplete fenced block
    if len(code_blocks) == 0:
        code_blocks = extract_incomplete_fenced_code(completion)
        if code_blocks:
            extracted_type = ExtractedType.IncompleteFenced

    # step3. if no incomplete fenced block found, try heuristic method to extract code
    if len(code_blocks) == 0:
        code_blocks = extract_heuristic_code(completion, language)
        if code_blocks:
            extracted_type = ExtractedType.Heuristic

    if kwargs.get('code_block_idx') is not None:
        try:
            completion = code_blocks[kwargs['code_block_idx']].code.replace('\r', '')
        except:
            completion = ''
    elif first_block_only:
        if code_blocks:
            completion = code_blocks[0].code.replace('\r', '')
        else:
            completion = ''
    else:
        completion = '\n\n'.join([b.code for b in code_blocks]).replace('\r', '')

    is_ut = kwargs.get('is_ut') == True
    if not is_ut:
        completion = postprocess_completion_v2(completion, language, no_removal, completion_bk, **kwargs)

    if '# Example usage' in completion:
        next_line = completion.index('# Example usage')
        completion = completion[:next_line].strip()

    return (completion, extracted_type.value)


def postprocess_completion_v2(completion: str, language: str, no_removal: bool, completion_bk: str, **kwargs) -> str:
    """Apply language-specific post-processing to extracted code.

    Cleans up extracted code based on the target language:

    - **Python**: Removes ``__main__`` blocks; optionally removes asserts.
    - **C/C++**: Removes ``int main()`` and everything after.
    - **Java**: Wraps in ``class Solution``, handles imports, optionally
      extracts inner function body.
    - **Go**: Removes ``package main`` (unless ``no_removal``), removes
      ``func main``.
    - **Scala**: Extracts body from ``object X { ... }`` wrapper.
    - **Verilog**: Extracts content between module ports and ``endmodule``.
    - **C#**: Optionally extracts inner function body.
    - **Kotlin**: Removes ``fun main`` function.

    Args:
        completion: The extracted code string to post-process.
        language: The canonical language name.
        no_removal: If True, skip certain removal steps (e.g., Go's
            ``package main``).
        completion_bk: The original unprocessed completion, used as fallback
            when extraction yields empty results (Verilog).
        **kwargs: Additional options (``inner_function_only``, ``remove_asserts``,
            ``entry_point``, ``declaration``).

    Returns:
        The post-processed code string.
    """
    inner_function_only = kwargs.get('inner_function_only') == True

    if language == 'python':
        lines = completion.splitlines()
        idx = None
        for i, line in enumerate(lines):
            if '__name__' in line and '__main__' in line:
                idx = i
                break
        if idx is not None:
            lines = lines[:idx]
        completion = '\n'.join(lines)

        if kwargs.get('remove_asserts') is True:
            lines = []
            for line in completion.splitlines():
                if not line.startswith('assert '):
                    lines.append(line)
            completion = '\n'.join(lines)

    elif language in ['cpp', 'c']:
        if 'int main()' in completion:
            next_line = completion.index('int main()')
            completion = completion[:next_line].strip()
    elif language == 'java':
        if inner_function_only:
            pattern = r"(public|private|protected)\s+(static\s+)(.*?)\((.*?)\)\s*{"
            body = find_inner_function_body(pattern, completion)
            if body is not None:
                completion = completion[body[0]:body[1]]
        else:
            # Add class Solution before signature
            if 'public class Main {\n' in completion:
                completion = completion.replace('public class Main {\n', 'class Solution {\n')
                completion = completion.replace('public static void main(String[] args)', '')
            # remove `public` of class `Solution`
            if 'public class Solution {' in completion:
                completion = completion.replace('public class Solution {', 'class Solution {')
            if 'class Solution' not in completion:
                for line in completion.split('\n'):
                    if kwargs.get('entry_point') and kwargs.get('entry_point') in line:
                        completion = completion.replace(line, 'class Solution {\n' + line)
                        completion += '\n}'
                        break
            # Add import statements
            for line in kwargs.get('declaration', '').split('\n'):
                if 'import' in line:
                    completion = line + '\n' + completion
    elif language == 'go':
        # 一般来说移除 `package main` 语句，部分数据集不要做移除，例如 mbxp
        if no_removal == False:
            completion = completion.replace('package main', '')

        # Delete the main function from completion, if exists.
        pattern = r"func\s+main\(.*?\)\s*{"
        body = find_inner_function_body(pattern, completion)
        if body is not None:
            completion = completion[:body[0]] + completion[body[1]:]
    elif language == 'scala':
        # 提取出包裹在 object X { ... } 中的部分，一般来说是个函数
        pat = r"object\s+\w+(\s+extends\s+\w+)?\s*\n*\{(.*)\}"
        r = re.findall(pat, completion, re.DOTALL | re.MULTILINE)
        if r:
            completion = r[0][1]
    elif language == 'verilog':
        # 提取出在 module X (X, X); .... endmodule 中分号到endmodule之间的内容，包括endmodule
        pat = r'module\s+\w+\s+\((.*?)\);(.*?)endmodule'
        r = re.findall(pat, completion, re.DOTALL | re.MULTILINE)
        if r:
            completion = r[0][1] + '\nendmodule'
        if completion == "":
            # if we cannot extract any code block, return the unacted input
            completion = completion_bk
    elif language == 'csharp':
        # 提取 class 内 function body 部分
        if inner_function_only:
            pattern = r"(public|private|protected|internal)\s+(static\s+)(.*?)\((.*?)\)\s*{"
            body = find_inner_function_body(pattern, completion)
            if body is not None:
                completion = completion[body[0]:body[1]]
    elif language == 'kotlin':
        # Delete the main function from completion, if exists.
        pattern = r"fun\s+main\(.*?\)\s*{"
        body = find_inner_function_body(pattern, completion)
        if body is not None:
            completion = completion[:body[0]] + completion[body[1]:]
    return completion


def trim_till_first_function(code, language):
    """Find the first function definition in code and return only that function.

    Locates the first function using language-specific regex patterns, then
    determines the function's full extent using bracket matching (for Go and
    TypeScript) or indentation analysis (for Python).

    Supported languages: ``'python'``, ``'golang'``/``'go'``, ``'typescript'``.

    Args:
        code: The source code string to search.
        language: The language of the code. Must be one of the supported
            languages.

    Returns:
        The source code from the beginning of the file through the end of
        the first function, or empty string if no function is found.

    Raises:
        ValueError: If the language is not supported.
    """
    # Regex patterns to find the start of a function
    if language == 'python':
        pattern = r'\bdef\s+\w+\s*\((?:[^()]|\n)*\)\s*->?\s*[\w\[\],\s]*:'
        # Python uses indentation, not brackets
        open_bracket, close_bracket = ':', None
    elif language in ['golang', 'go']:
        pattern = r'\bfunc\s+\w+\s*\([^)]*\)\s*(\[\w+\]|\*?\w*)?\s*{'
        open_bracket, close_bracket = '{', '}'
    elif language == 'typescript':
        pattern = r'\bfunction\s+\w+\s*\([^)]*\)\s*[:\w\s]*{'
        open_bracket, close_bracket = '{', '}'
    else:
        raise ValueError("Unsupported language")

    # Find the start of the first function
    match = re.search(pattern, code)
    if not match:
        return ''  # No function found

    if close_bracket:
        # Count brackets to find the end
        start_index = match.start()
        end_index = start_index
        bracket_count = 0
        in_string = False
        escape = False
        while end_index < len(code):
            char = code[end_index]
            if char in ('"', "'"):
                # Handle strings
                if not in_string:
                    in_string = True
                    string_delimiter = char
                elif char == string_delimiter and not escape:
                    in_string = False
            elif not in_string:
                if char == open_bracket:
                    bracket_count += 1
                elif char == close_bracket:
                    bracket_count -= 1
                    if bracket_count == 0:
                        break
            # Handle escape characters in strings
            escape = char == '\\' and not escape
            end_index += 1

        return code[:end_index + 1]
    else:
        # For Python, use indentation levels
        lines = code[match.end():].splitlines()
        first_line_indent = len(lines[0]) - len(lines[0].lstrip())
        function_code = code[:match.end()]
        for line in lines[1:]:
            indent = len(line) - len(line.lstrip())
            if line.strip() and indent <= first_line_indent:
                break
            function_code += '\n' + line

        return function_code


def find_java_public_class_name(java_code: str) -> str:
    """
    Finds and returns the name of the public class in a given Java source code string.
    If no public class is found, returns None.

    Args:
    java_code (str): A string containing Java source code.

    Returns:
    str or None: The name of the public class if found, otherwise None.
    """
    pattern = r'\bpublic\s+(abstract\s+|final\s+)?class\s+(\w+)'
    match = re.search(pattern, java_code)
    if match:
        return match.group(2)
    else:
        return None


def find_inner_function_body(signature_pattern: str, completion: str) -> Optional[Tuple[int, int]]:
    """
    Finds inner function body inside some class/namespace block.
    Used for language like: c#, java, etc.

    Args:
    signature_pattern (str): Function signature pattern, includes the left curly brackets, used to find the starting position of function.

    Returns:
    Tuple[int, int] or None: The function body indices if exists, otherwise None.
    """
    matches = re.search(signature_pattern, completion, re.DOTALL | re.MULTILINE)
    if matches is None:
        return None
    brackets_count = 1
    idx = None
    for idx in range(matches.end(), len(completion)):
        if completion[idx] == '{':
            brackets_count += 1
        elif completion[idx] == '}':
            brackets_count -= 1

        if brackets_count == 0:
            break
    if idx is None or brackets_count != 0:
        return None
    # body = completion[matches.start():idx + 1]
    return (matches.start(), idx + 1)
