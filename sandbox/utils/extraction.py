"""Code extraction utilities for parsing code from LLM completions.

Provides a multi-strategy pipeline for extracting code blocks from freeform text
generated by language models. Strategies include fenced code block parsing
(complete and incomplete), language-specific heuristic extraction, and
user-defined custom extraction logic. Also includes post-processing functions
for cleaning extracted code (removing entry points, wrapping in classes, etc.)
across many programming languages.
"""

import re
from enum import Enum
from typing import List, Literal, Optional, Tuple

import structlog
from pydantic import BaseModel

from sandbox.runners.types import Language

logger = structlog.stdlib.get_logger()

NullableLang = Language | Literal['']


class ExtractedType(Enum):
    """Classification of how a code block was extracted from completion text.

    Attributes:
        Fenced: Extracted from a complete fenced code block (````` ``` ... ``` `````).
        IncompleteFenced: Extracted from a fenced block missing the closing fence.
        Heuristic: Extracted using language-specific heuristic patterns.
        Empty: No code block was found in the completion.
    """

    Fenced = 'fenced'
    IncompleteFenced = 'incomplete_fenced'
    Heuristic = 'heuristic'
    Empty = 'empty'


class CodeBlock(BaseModel):
    """A single extracted code block with associated metadata.

    Attributes:
        priority: Numeric priority used to rank extraction methods. Higher values
            indicate more reliable extraction (e.g., fenced=30, incomplete=20,
            heuristic=10).
        language: The canonical language identifier (e.g., ``'python'``, ``'java'``),
            or empty string if the language could not be determined.
        code: The extracted source code content.
    """

    priority: int
    language: str
    code: str


fenced_code_block_pattern = re.compile(
    # Starting with three backticks and optional language identifier
    r'```([^\n]*)\n'
    r'(.*?)'  # Non-greedy capture of the content
    r'\n\s*```',  # Ending with three backticks
    re.DOTALL | re.MULTILINE)

incomplete_fenced_code_block_pattern = re.compile(
    # Starting with three backticks and optional language identifier
    r'```([^\n]*)\n'
    r'(.*)',  # Greedy capture of the content
    re.DOTALL | re.MULTILINE)

language_to_aliases = {
    'python': ['python', 'Python', 'py', 'Python3', 'python3', 'PY'],
    'cpp': ['cpp', 'c++', 'C++', 'Cpp', 'CPP'],
    'nodejs': ['javascript', 'Javascript', 'JavaScript', 'JS', 'js'],
    'go': ['go', 'Go'],
    'java': ['java', 'Java'],
    'php': ['php'],
    'csharp': ['csharp', 'c#', 'C#'],
    'bash': ['bash', 'Bash', 'BASH', 'sh', 'shell'],
    'typescript': ['typescript'],
    'rust': ['rust', 'Rust', 'rs'],
    'sql': ['sql', 'SQL', 'Sql'],
    'D': ['D', 'd'],
    'julia': ['julia', 'Julia', 'jl'],
    'lua': ['lua', 'Lua'],
    'php': ['php', 'PHP'],
    'perl': ['perl', 'Perl', 'PERL'],
    'R': ['R', 'r'],
    'ruby': ['ruby', 'Ruby'],
    'rust': ['rust', 'Rust', 'rs'],
    'scala': ['scala', 'Scala'],
    'kotlin': ['kotlin', 'Kotlin'],
    'c': ['c', 'C'],
    'html': ['html', 'Html', 'HTML'],
    'javascript': ['javascript', 'Javascript', 'JavaScript'],
    'verilog': ['verilog', 'Verilog', 'VERILOG'],
    'racket': ['racket'],
    'swift': ['swift'],
}

aliases_to_language_tiled = {v: k for k, vs in language_to_aliases.items() for v in vs}


# code extraction
def extract_fenced_code(completion: str) -> List[CodeBlock]:
    """Extract all complete fenced code blocks from a completion string.

    Matches blocks delimited by triple backticks with an optional language
    tag (e.g., ````` ```python ... ``` `````). Each match is returned as a
    ``CodeBlock`` with priority 30.

    Args:
        completion: The raw LLM completion text to search.

    Returns:
        A list of ``CodeBlock`` objects for each complete fenced code block
        found, with language aliases resolved to canonical names.
    """
    code_matches = re.findall(fenced_code_block_pattern, completion)
    results = []
    for m in code_matches:
        lang = aliases_to_language_tiled.get(m[0].strip(), '')
        results.append(CodeBlock(priority=30, language=lang, code=m[1]))
    return results


def adjust_code_block(code_blocks: List[CodeBlock], language: str) -> List[CodeBlock]:
    """Fix code blocks where the language tag appears as the first line of code.

    Some models (notably llama3) produce fenced blocks where the language
    identifier ends up as the first line of the code content rather than
    on the fence line itself. This function detects that pattern and moves
    the language tag to the block's ``language`` field, removing it from
    the code.

    Args:
        code_blocks: The list of code blocks to adjust.
        language: The target canonical language name to check against.

    Returns:
        The adjusted list of code blocks.
    """
    if language == '' or language not in language_to_aliases:
        return code_blocks
    ret = []
    for block in code_blocks:
        lines = block.code.splitlines()
        if block.language == '' and lines and lines[0].strip() in language_to_aliases[language]:
            block.language = language
            block.code = '\n'.join(lines[1:])
        ret.append(block)
    return ret


# code extraction


def extract_incomplete_fenced_code(completion: str) -> List[CodeBlock]:
    """Extract fenced code blocks that are missing the closing triple backticks.

    Matches blocks that start with triple backticks and an optional language
    tag but have no closing fence (e.g., when the LLM output was truncated).
    Uses greedy matching to capture all remaining content. Each match is
    returned as a ``CodeBlock`` with priority 20 (lower than complete fenced).

    Args:
        completion: The raw LLM completion text to search.

    Returns:
        A list of ``CodeBlock`` objects for each incomplete fenced code block
        found.
    """
    code_matches = re.findall(incomplete_fenced_code_block_pattern, completion)
    results = []
    for m in code_matches:
        lang = aliases_to_language_tiled.get(m[0].strip(), '')
        results.append(CodeBlock(priority=20, language=lang, code=m[1]))
    return results


def extract_heuristic_code(completion: str, language: NullableLang = '') -> List[CodeBlock]:
    """Extract code blocks using language-specific heuristic patterns.

    Applies pattern-based extraction when fenced code blocks are not present.
    Currently supports:

    - **Python**: Matches ``def``/``class`` definitions with optional imports.
    - **SQL**: Matches ``SELECT``/``WITH ... AS`` statements.
    - **Bash**: Returns the entire completion as a single code block.

    Each match is returned as a ``CodeBlock`` with priority 10 (lowest).

    Args:
        completion: The raw LLM completion text to search.
        language: The target language for heuristic extraction. Only
            ``'python'``, ``'sql'``, and ``'bash'`` are supported; other
            languages return an empty list.

    Returns:
        A list of ``CodeBlock`` objects extracted via heuristics.
    """

    def extract_py(text):
        code = "\n".join([line for line in text.split("\n") if line.strip() != ""]) + "\n"

        pattern_py = "(?:^(?:import|from|#)[^\n]+\n)*" \
            "^(?:def|class) [^\n]+\n" \
            r"(?:\s+[^\n]+\n)+"  # 函数/类实现
        matches = re.findall(pattern_py, code, re.M)
        return matches

    def extract_sql(text):
        code = "\n".join([line for line in text.split("\n") if line.strip() != ""]) + "\n"

        pattern_sql = r"^\s*(?:select|with\s[^\n]+as)[^;]*"
        matches = re.findall(pattern_sql, code, re.M | re.IGNORECASE)
        return matches

    def extract_bash(text):
        code = "\n".join([line for line in text.split("\n") if line.strip() != ""]) + "\n"
        return code

    if language == 'python':
        return [CodeBlock(priority=10, language='python', code=m) for m in extract_py(completion)]
    elif language == 'sql':
        return [CodeBlock(priority=10, language='sql', code=m) for m in extract_sql(completion)]
    elif language == 'bash':
        return [CodeBlock(priority=10, language='bash', code=extract_bash(completion))]
    else:
        return []


def extract_custom_code(completion: str, custom_logic: str) -> List[CodeBlock]:
    """Execute user-provided Python logic to extract code blocks.

    Runs the ``custom_logic`` string as Python code via ``exec()``, providing
    it with a context that includes ``CodeBlock``, the ``completion`` text,
    ``submit_code_blocks`` (a callback to submit extracted blocks), and the
    built-in extraction helpers ``extract_fenced_code`` and
    ``extract_heuristic_code``.

    Args:
        completion: The raw LLM completion text to extract from.
        custom_logic: A string of Python code that calls
            ``submit_code_blocks(list_of_CodeBlock)`` to emit results.

    Returns:
        A list of ``CodeBlock`` objects produced by the custom logic.
    """
    blocks = []

    def submit(cbs):
        for cb in cbs:
            assert isinstance(cb, CodeBlock), 'extrace code type must be class CodeBlock'
            blocks.append(cb)

    context = {
        'CodeBlock': CodeBlock,
        'completion': completion,
        'submit_code_blocks': submit,
        'extract_fenced_code': extract_fenced_code,
        'extract_heuristic_code': extract_heuristic_code,
    }
    exec(custom_logic, context)
    logger.info(f'got {len(blocks)} custom code blocks')
    return blocks


def filter_language(blocks: List[CodeBlock], language: NullableLang) -> List[CodeBlock]:
    """Filter a list of code blocks to only those matching a given language.

    Args:
        blocks: The list of ``CodeBlock`` objects to filter.
        language: The canonical language name to match, or empty string to
            match blocks with no detected language.

    Returns:
        A new list containing only the blocks whose language matches.
    """
    return [b for b in blocks if b.language == language]


